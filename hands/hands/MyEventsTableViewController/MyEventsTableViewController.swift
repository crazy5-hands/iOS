import UIKit

class MyEventsTableViewController: UITableViewController {

    var objects = [EventViewModel]()
    
    override func viewDidLoad() {
        super.viewDidLoad()
        objects = EventViewModel.load()
        
        // Uncomment the following line to preserve selection between presentations
        // self.clearsSelectionOnViewWillAppear = false

        // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
        // self.navigationItem.rightBarButtonItem = self.editButtonItem
        let refreshControl = UIRefreshControl()
        refreshControl.addTarget(self, action: #selector(MyEventsTableViewController.refreshControlValueChanged(sender:)), for: .valueChanged)
        self.tableView.addSubview(refreshControl)
    }

    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
    }
    
    func refreshControlValueChanged(sender: UIRefreshControl) {
        objects.removeAll()
        objects = EventViewModel.load()
        self.tableView.reloadData()
        sender.endRefreshing()
    }

    override func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return objects.count
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "cell", for: indexPath) as! MyEventTableViewCell
        cell.update(objects[indexPath.row])
        return cell
    }

